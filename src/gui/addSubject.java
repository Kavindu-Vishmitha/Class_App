    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {

            String addSubject = subjectTextField.getText();
            String addFee = subjectFeeTextField.getText();

            if (addSubject.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter subject name", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            } else if (!addSubject.matches("[a-zA-Z ]+")) {
                JOptionPane.showMessageDialog(this, "Please enter only letters for subject name", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            } else if (addFee.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter subject fee", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            } else if (addFee.matches("\\d+")) {
                JOptionPane.showMessageDialog(this, "Please enter a valid subject fee", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }

            ResultSet rs1 = MySql.executeSearch("SELECT * FROM subject WHERE name = '" + addSubject + "'");
            
            if (rs1.next()) {
                JOptionPane.showMessageDialog(this, "Subject already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }

            MySql.executeUpdate("INSERT INTO subject(name,fee) VALUES ('" + addSubject + "','" + addFee + "')");

            JOptionPane.showMessageDialog(this, "Subject added successfully !", "Success", JOptionPane.INFORMATION_MESSAGE);

            if (teacherGui != null) {
                teacherGui.loadSubject();
            }

            this.dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Adding Subject: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            
            String selectedSubject = (String) subjectComboBox.getSelectedItem();
            String updatedFee = subjectFeeUpdateTextField.getText();

            if (selectedSubject == null || selectedSubject.equals("Select Subject")) {
                JOptionPane.showMessageDialog(this, "Please select a subject to update", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }

            if (updatedFee.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a subject fee to update", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }

            if (!updatedFee.matches("\\d+(\\.\\d{1,2})?")) {
                JOptionPane.showMessageDialog(this, "Please enter a valid numeric fee (e.g., 500 or 500.00)", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String subjectId = subjectMap.get(selectedSubject);

            MySql.executeUpdate("UPDATE subject SET fee = '" + updatedFee + "' WHERE id = '" + subjectId + "'");

            JOptionPane.showMessageDialog(this, "Subject fee updated successfully !", "Success", JOptionPane.INFORMATION_MESSAGE);

            loadSubject();

            subjectComboBox.setSelectedIndex(0);
            subjectFeeUpdateTextField.setText("");
            subjectFeeUpdateTextField.setEnabled(false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error updating subject fee: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


