    public void loadSubject() {

        try {

            ResultSet resultSet = MySql.executeSearch("SELECT * FROM `subject` ORDER BY `name` ASC");

            Vector<String> vector = new Vector<>();

            vector.add("Select Subject");

            while (resultSet.next()) {

                vector.add(resultSet.getString("name"));

                subjectMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);

            subjectComboBox.setModel(model);

        } catch (Exception e) {

            e.printStackTrace();

        }

    }

    private void setupSubjectComboBoxListener() {
        
        subjectComboBox.addActionListener(new ActionListener() {
            
            @Override
            public void actionPerformed(ActionEvent e) {
                
                String selectedSubject = (String) subjectComboBox.getSelectedItem();

                if (!selectedSubject.equals("Select Subject")) {
                    String subjectId = subjectMap.get(selectedSubject);

                    try {
                        
                        ResultSet rs = MySql.executeSearch("SELECT `fee` FROM `subject` WHERE `id` = '" + subjectId + "'");

                        if (rs.next()) {
                            double feeValue = rs.getDouble("fee");
                            DecimalFormat df = new DecimalFormat("0.00");
                            String formattedFee = df.format(feeValue);

                            subjectFeeUpdateTextField.setText(formattedFee);
                            subjectFeeUpdateTextField.setEnabled(true);
                        }

                    } catch (Exception ex) {
                        ex.printStackTrace();
                    }
                } else {
                    subjectFeeUpdateTextField.setText("");
                }
            }
        });
    }

